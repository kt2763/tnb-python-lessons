a, b = 7, 3
print(a + b)  # 10
print(a - b)  # 4
print(a * b)  # 21
print(a / b)  # 2.3333...
print(a // b)  # 2 （整数除算）
print(a % b)  # 1
print(a**b)  # 343

a = 6  # 110 (2進数)
b = 3  # 011 (2進数)

print(a & b)  # 2  (010)
print(a | b)  # 7  (111)
print(a ^ b)  # 5  (101)
print(~a)  # -7  (6 のビットを反転)
